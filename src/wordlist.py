def load_wordlist(file_path: Optional[str] = None) -> List[str]:
    """
    Load wordlist from EFF file or return fallback.
    Handles EFF format: "11111\tabacus" (code + tab + word)
    """
    if file_path is None:
        file_path = Path(__file__).parent.parent / "data" / "eff_wordlist.txt"

    try:
        with open(file_path, encoding="utf-8") as f:
            words = []
            for line in f:
                line = line.strip()
                if not line:
                    continue
                # EFF формат: "11111<tab>abacus" — берём второе поле
                if "\t" in line:
                    parts = line.split("\t", 1)
                    if len(parts) == 2 and parts[1]:
                        word = parts[1].strip().lower()
                        if word and word.isalpha() and len(word) >= 3:
                            words.append(word)
                # Простой формат: просто слово в строке
                elif line.isalpha() and len(line) >= 3:
                    words.append(line.lower())

            if words:
                logger.info(f"Loaded {len(words)} words from {file_path}")
                return words
            else:
                logger.warning(f"No valid words found in {file_path}")

    except FileNotFoundError:
        logger.warning(f"Wordlist file not found: {file_path}, using fallback")
    except Exception as e:
        logger.error(f"Error loading wordlist: {e}")

    # Fallback: встроенный список
    logger.info(f"Using fallback wordlist ({len(_FALLBACK_WORDS)} words)")
    return _FALLBACK_WORDS.copy()


